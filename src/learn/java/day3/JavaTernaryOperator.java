package learn.java.day3;
//Java Ternary Operator Example
public class JavaTernaryOperator {

    public static void main(String args[]) {
        int a = 2;
        int b = 5;
        int min=(a<b)?a:b;

        System.out.println(min);

        // Another Example
        int c = 10;
        int d = 5;
        int min1=(c<d)?c:d;

        System.out.println(min1);
    }
}
